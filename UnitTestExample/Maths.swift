import UIKit

class Maths {
    
    func add(num1: Int, num2: Int) -> Int {
        let sum = num1 + num2
        return sum
    }
    
    func subtract(num1: Int, num2: Int) -> Int {
        let diffrence = num1 - num2
        return diffrence
    }
    
    func multiply(num1: Int, num2: Int) -> Int {
        let product = num1 * num2
        return product
    }
    
    func divide(num1: Int, num2: Int) -> Int {
        let quotient = num1 / num2
        return quotient
    }
}
